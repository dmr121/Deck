/// Represents the physical direction of a card swipe.
public enum SwipeDirection {
    /// A swipe to the right edge of the screen.
    case right
    /// A swipe to the left edge of the screen.
    case left
    /// A swipe to the top edge of the screen.
    case up
    /// A swipe to the bottom edge of the screen.
    case down
}
